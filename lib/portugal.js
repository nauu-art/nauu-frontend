export const getDistritoFromConcelho = (concelho) => {
  if (!concelho) return null
  for (const [distrito, concelhos] of Object.entries(DISTRITOS_CONCELHOS)) {
    if (concelhos.some(c => c.toLowerCase() === concelho.toLowerCase())) return distrito
    if (distrito.toLowerCase() === concelho.toLowerCase()) return distrito
  }
  return null
}

